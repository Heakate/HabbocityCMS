        Il y a plusieurs manières d'améliorer ton tchat sur HabboCity. Apprends toutes les commandes ou juste deux
        ou
        trois d'entre elles: de toute façon les voilà toutes regroupées ici dans cette liste bien pratique.
        <br><br>

        <div class="moduleHelpPopupTitleCommands">Commandes tchat</div>
        <b>Maintiens la touche shit en appuyant sur la touche entrée</b> : permet d'écrire en gras.<br>
        <b>":crier"</b> permet aussi d'écrire en gras.<br>
        <b>":chut &lt;username&gt;"</b> murmure en privé à l'utilisateur cible.<br>
        <b>":dire &lt;message&gt;"</b> permet d'écrire dans le tchat, cette commande n'est pas très utile.<br>
        <b>":sms &lt;username&gt; &lt;message&gt;"</b> envoie un sms à l'utilisateur cible.<br>
        <b>":sign &lt;0 à 17&gt;"</b> ton habbo porte un panneau.<br>
        <b>":quickpoll &lt;message&gt;"</b> crée un sondage d'une durée de 45 secondes.<br>
        <b>":invite &lt;message&gt;"</b> envoie un message à tous tes amis.<br>
        <b>":groupnotif &lt;message&gt;"</b> envoie un message à tous les membres du groupe se situant dans ton
        appartement.<br>
        <b>":chooser"</b> affiche une liste des joueurs présents dans ton appartement.<br>
        <b>":nomention"</b> active/désactive les mentions.<br>
        <b>"@&lt;username&gt; &lt;message&gt;"</b> envoie une mention à la cible de cette commande.<br>
        <br>
        <b>":sit"</b> ton habbo s'assoit.<br>
        <b>":lay"</b> ton habbo s'allonge.<br>
        <b>":stand"</b> ton habbo se relève.<br>
        <b>":jump"</b> ton habbo saute.<br>
        <b>":idle"</b> ton habbo s'endort.<br>
        <b>":eyes"</b> ton habbo perd ses yeux.<br>
        <b>":noface"</b> identique à <b>":eyes"</b>.<br>
        <b>":kiss"</b> ton habbo fait un bisou.<br>
        <b>":punch &lt;username&gt;"</b> ton habbo frappe un autre habbo.<br>
        <b>":moonwalk"</b> ton habbo marche à l'envers.<br>
        <br>
        <b>Ces commandes dites "smileys" appliqueront une animation à ton habbo durant un court instant.</b><br>
        <b>"o/"</b> ton habbo salue.<br>
        <b>"_b"</b> ton habbo lève son pouce.<br>
        <b>":)"</b> ton habbo sourit.<br>
        <b>"&gt;:("</b> ton habbo est en colère.<br>
        <b>":O"</b> ton habbo est surpris, fonctionne aussi en minuscule.<br>
        <b>":D"</b> ton habbo rigole, fonctionne aussi en minuscule.<br>
        <br>
        <b>": x"</b> en cliquant sur un joueur en même temps, permet de citer son pseudo en un éclair.<br>
        <br>

        <div class="moduleHelpPopupTitleCommands">Commandes de construction</div>
        <b>":furni"</b> affiche une liste des mobiliers présents dans ton appartement.<br>
        <b>":position"</b> indique ta position x, y et z, ainsi que la rotation de ton habbo.<br>
        <b>":build"</b> affiche un outil qui permet de modifier la rotation, la hauteur et l'état des prochains
        mobiliers posés.<br>
        <b>":forcerot &lt;0 à 7&gt;"</b> change la rotation par défaut des prochains mobiliers posés. <b>":forcerot
            stop"</b> pour arrêter la commande.<br>
        <b>":forceact &lt;0 à 20&gt;"</b> change l'état par défaut des prochains mobiliers posés. <b>":forceact
            stop"</b> pour arrêter la commande.<br>
        <b>":setz &lt;-30 à 100&gt;"</b> change la hauteur par défaut des mobis, nombre à virgule accepté. <b>":setz
            stop"</b> pour arrêter la commande.<br>
        <b>":construit &lt;0 à 100&gt;"</b> change l'espacement par défaut entre deux mobiliers, nombre à virgule
        accepté. <b>":construit stop"</b> pour arrêter la commande.<br>
        <b>":modebuild"</b> donne la possibilité de poser un mobilier sur un autre quoi qu'il arrive. <b>":modebuild
            stop"</b> pour arrêter la commande.<br>
        <b>":floor"</b> ouvre l'éditeur de sol.<br>
        <b>":bcfloor"</b> ouvre aussi l'éditeur de sol.<br>
        <b>":noitemfloor"</b> autorise l'édition des dalles comportant des mobiliers dessus avec le floor.<br>
        <b>":floornoitems"</b> retire toutes les cases ne possédant pas de mobilier.<br>
        <b>":alldeleteitem"</b> retire tous les mobiliers présents sur ta case.<br>
        <b>":diagonal"</b> retire la marche en diagonale dans ton appart, réécris la commande pour annuler ce mode.<br>
        <b>":wired"</b> affiche/cache les wireds.<br>
        <b>":pickall"</b> retire tous les mobiliers de ton appartement.<br>
        <b>":ejectall"</b> retire tous TES mobiliers dans un appartement où tu es accrédité, attention : si tu es
        proprio la commande retirera les mobiliers ne t'appartenant pas.<br>
        <br>
        <b>":empty"</b> efface ton inventaire, confirme la commande avec <b>":empty oui"</b>.<br>
        <b>":emptypets"</b> efface les animaux de ton inventaire, confirme la commande avec <b>":emptypets
            oui"</b>.<br>
        <b>":emptybots"</b> efface les bots ton inventaire, confirme la commande avec <b>":emptybots oui"</b>.<br>
        <br>
        <b>":lock"</b> met une sonnerie à ton appartement, la retire s'il y en avait déjà une.<br>
        <b>":setmax &lt;1 à 200&gt;"</b> ajuste le nombre possible d'utilisateurs dans ton appartement.<br>
        <b>":setspeed &lt;1 à 500&gt;"</b> ajuste la vitesse des rollers de l'appartement.<br>
        <b>":kickpower &lt;1 à 7&gt;"</b> change la puissance de frappe sur un ballon ( en nombre de cases. )<br>
        <b>":roomkeb"</b> rend aléatoire le "keb" entre deux joueurs.<br>
        <b>":wiredlimit"</b> débride les wireds : les wireds seront activables autant de fois que l'on veut.<br>
        <br>
        <b>":screenshot"</b> prend en photo l'appartement dans lequel tu te situes.<br>
        <b>":zoom &lt;0 à 2147483647&gt;"</b> permet de zoomer/dézoomer.<br>
        <br>

        <div class="moduleHelpPopupTitleCommands">Commandes de modération et paramètres</div>
        <b>":noalert"</b> désactive les alertes des animations, réécris la commande pour réactiver.<br>
        <b>":mute &lt;username&gt;"</b> met en sourdine la cible de cette commande pour deux minutes.<br>
        <b>":shutup &lt;username&gt;"</b> même action que <b>:mute</b>.<br>
        <b>":kick &lt;username&gt;"</b> kick la cible de cette commande.<br>
        <b>":2 &lt;username&gt;"</b> même action que <b>:kick</b>.<br>
        <b>":roomkick"</b> kick l'entièreté des utilisateurs de l'appartement, sauf accrédités.<br>
        <b>":freeze &lt;username&gt;"</b> freeze la cible de cette commande.<br>
        <b>":roomfreeze"</b> freeze l'entièreté des utilisateurs de l'appartement, réécrire la commande pour
        annuler.<br>
        <b>":warp &lt;username&gt;"</b> téléporte la cible de cette commande sur ta case.<br>
        <b>":warproom"</b> téléporte l'entièreté des utilisateurs de l'appartement, sauf accrédités.<br>
        <b>":teleport"</b> permet de te téléporter instantanément sur la case que tu cliques.<br>
        <b>":tele"</b> identique à <b>":teleport"</b>.<br>
        <b>":noenable &lt;username&gt;"</b> retire l'effet au joueur ciblé.<br>
        <b>":notransf &lt;username&gt;"</b> retire la transformation au joueur ciblé.<br>
        <b>":emptyfriends"</b> retire tous tes amis.<br>
        <b>":deletegroup"</b> supprime le groupe étant dans l'appartement où tu te situes.<br>
        <br>

        <div class="moduleHelpPopupTitleCommands">Commandes pour les bots</div>
        <b>":botenable &lt;ID de l'effet&gt; &lt;botname&gt;"</b> donne un effet au bot. ( liste des effets avec <b>":help
            enables"</b>. )<br>
        <b>":bothanditem &lt;ID de l'objet&gt; &lt;botname&gt;"</b> donne un objet au bot. ( liste des objets avec
        <b>":help handitem"</b>. )<br>
        <b>":botsit &lt;botname&gt;"</b> le bot cible s'assoit.<br>
        <b>":botlay &lt;botname&gt;"</b> le bot cible s'allonge.<br>
        <b>":botcontrol &lt;botname&gt;"</b> tu contrôles le déplacement du bot cible, <b>":botcontrol stop"</b> pour
        arrêter.<br>
        <b>":botfollow &lt;botname&gt;"</b> le bot te suit.<br>
        <b>":botsmute &lt;botname&gt;"</b> met en sourdine le bot cible, il pourra tout de même parler par le biais
        des wireds bot.<br>
        <b>":mimicbot &lt;botname&gt;"</b> copie le look du bot cible.<br>
        <br>

        <div class="moduleHelpPopupTitleCommands">Commandes d'aide</div>
        <b>":commands"</b> permet d'afficher... cette liste de commandes...<br>
        <b>":help"</b> affiche le référencement des aides.<br>
        <b>":help wired"</b> affiche une documentation sur les wireds.<br>
        <b>":help enables"</b> affiche la liste des enables.<br>
        <b>":help handitems"</b> affiche la liste des handitems.<br>
        <b>":help magicbox"</b> affiche le contenu des boxs.<br>
        <b>":help superwired"</b> affiche la liste des fonctions des effets et conditions superwired.<br>
        <b>":help transf"</b> affiche la liste des transforms.<br>

        <br>
        <div class="moduleHelpPopupTitleCommands">Commandes amusantes</div>
        <b>":pushmode"</b> donne la possibilité d'utiliser les commandes ci-dessous, réécris la commande pour désactiver
        ce mode.<br>
        <b>":push &lt;username&gt;"</b> pousse l'utilisateur cible.<br>
        <b>":pull &lt;username&gt;"</b> tire l'utilisateur cible.<br>
        <b>":spull &lt;username&gt;"</b> tire l'utilisateur cible de loin.<br>
        <br>
        <b>":enable &lt;ID de l'effet&gt;"</b> donne un effet à ton habbo. ( liste des effets avec <b>":help
            enables"</b>, retire ton enable avec <b>":enable stop"</b>. )<br>
        <b>":enable freeze"</b> permet de conserver l'effet porté même après avoir changé d'appartement.<br>
        <b>":handitem &lt;ID de l'objet&gt;"</b> donne un objet à ton habbo. ( liste des objets avec <b>":help
            handitem"</b>. )<br>
        <b>":transform &lt;nom de la transformation&gt;"</b> transforme ton habbo. ( liste des transformations avec
        <b>":help transf"</b>. )<br>
        <b>":cars"</b> ton habbo prend la voiture ( tu te déplaces deux fois plus vite, redis la commande pour
        désactiver ce mode. )<br>
        <b>":mimic &lt;username&gt;"</b> copie l'apparence de la cible de cette commande.<br>
        <b>":follow &lt;username&gt;"</b> permet de rejoindre la cible de cette commande.<br>
        <b>":shake"</b> fait bouger l'écran dans tous les sens ( visible par toi seulement ).<br>
        <b>":iddqd"</b> retourne ton écran ( visible par toi seulement ), utilise <b>":zoom 1"</b> pour remettre l'écran
        à l'endroit.<br>
        <b>":hidemouse"</b> fait disparaître ton curseur, réécris la commande pour le faire réapparaître.<br>
        <br>

